package com.tencent.bk.base.datahub.hubmgr.rest.dto;

import com.tencent.bk.base.datahub.hubmgr.rest.dto.custom.annotation.ConfigValidate;
import java.util.List;
import java.util.Set;
import javax.validation.constraints.NotBlank;
import javax.validation.constraints.NotNull;

public class QueryKeysParam extends ClusterParam {

    @NotBlank
    @ConfigValidate
    private String cacheName;

    @NotNull
    private Set<String> keys;

    @NotNull
    private List<String> columns;

    public QueryKeysParam(String clusterName, String url, String cacheName, Set<String> keys, List<String> columns) {
        this.clusterName = clusterName;
        this.url = url;
        this.cacheName = cacheName;
        this.keys = keys;
        this.columns = columns;
    }

    public Set<String> getKeys() {
        return keys;
    }

    public void setKeys(Set<String> keys) {
        this.keys = keys;
    }

    public String getCacheName() {
        return cacheName;
    }

    public void setCacheName(String cacheName) {
        this.cacheName = cacheName;
    }

    public List<String> getColumns() {
        return columns;
    }

    public void setColumns(List<String> columns) {
        this.columns = columns;
    }
}
