package com.tencent.bk.base.datalab.queryengine.server.util;

import org.reflections.Reflections;
import org.reflections.util.ClasspathHelper;
import org.reflections.util.ConfigurationBuilder;
import org.reflections.util.FilterBuilder;

/**
 * 反射工具类
 */
public class ReflectionUtil {

    /**
     * 获取 Reflections 实例
     *
     * @param packageName 包名
     * @param includePattern 要扫描的文件通配符
     * @return 返回 Reflections 实例
     */
    public static Reflections getReflections(String packageName, String includePattern) {
        return new Reflections(
                new ConfigurationBuilder()
                        .addUrls(ClasspathHelper.forPackage(packageName))
                        .filterInputsBy(new FilterBuilder.Include(includePattern)));
    }
}
